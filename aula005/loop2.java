package aula005;

public class Loop2 {
    public static void main(String[] args) {
        /*WHILE - APLICAÇÃO
         * while(condição){
         * bloco de comandos
         * }
         */
        int i=0;
        System.out.println("Início da contagem:");
        while(i<=10){
            System.out.printf("%d%n", i++);
        }
        System.out.println("Fim do programa...");
    }
}
