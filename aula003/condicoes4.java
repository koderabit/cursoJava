package aula003;
public class Condicoes4 {
    public static void main(String[] args) {
        int numSorte = 45;
        switch(numSorte){
            case 26: 
                System.out.println("O seu número da sorte é o 26");
                break;
            case 58: 
                System.out.println("O seu número da sorte é o 58");
                break;
            case 81: 
                System.out.println("O seu número da sorte é o 81");
                break;
            case 46: 
                System.out.println("O seu número da sorte é o 46");
                break;
            case 15: 
                System.out.println("O seu número da sorte é o 15");
                break;
            case 37: 
                System.out.println("O seu número da sorte é o 37");
                break;
            case 44: 
                System.out.println("O seu número da sorte é o 44"); 
                break;
            default: 
                System.out.println("Eu realmente não sei qual o seu número da sorte, mas que azar.");
                break;
        }
    }
}
